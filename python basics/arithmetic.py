# Arithmetic Operators in Python

a = 10
b = 3

print(f"Addition: {a} + {b} = {a + b}")        # Addition
print(f"Subtraction: {a} - {b} = {a - b}")     # Subtraction
print(f"Multiplication: {a} * {b} = {a * b}")  # Multiplication
print(f"Division: {a} / {b} = {a / b}")        # Division (float result)
print(f"Floor Division: {a} // {b} = {a // b}") # Floor Division (integer result)
print(f"Modulus: {a} % {b} = {a % b}")         # Modulus (remainder)
print(f"Exponentiation: {a} ** {b} = {a ** b}") # Exponentiation (power)


